# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam
# o número recebido como parâmetro.

def duplicar(numero):
    return numero * 2
def triplicar(numero):
    return numero * 3
def quadriplicar(numero):
    return numero * 4


vezes_dois = duplicar(3)
vezes_tres = triplicar(3)
vezes_quatro = quadriplicar(3)

print(vezes_dois)
print(vezes_tres)
print(vezes_quatro)